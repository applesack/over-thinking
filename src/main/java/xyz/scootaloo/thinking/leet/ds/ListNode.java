package xyz.scootaloo.thinking.leet.ds;

/**
 * @author AppleSack
 * @since 2023/02/16
 */
public class ListNode {

    public int      val;
    public ListNode next;

    ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
